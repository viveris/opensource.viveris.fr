Tunnel IP/ROHC<br>tunnel point &agrave; point avec compression ROHC
</h1>

<h2>Pr&eacute;sentation</h2>
<p>Le tunnel IP/ROHC &eacute;tablit un tunnel point &agrave; point entre deux
   h&ocirc;tes r&eacute;seaux, puis route tout leur trafic au travers du tunnel.
   Les paquets ROHC sont encapsul&eacute;s dans des paquets IP directement
   (afin d'&eacute;viter le surco&ucirc;t engendr&eacute; par les ent&ecirc;tes
   UDP ou GRE par exemple). Le nom «&nbsp;IP/ROHC&nbsp;» vient de cette
   sp&eacute;cificit&eacute;.</p>
<p>En plus de la compression ROHC, le tunnel IP/ROHC aggr&egrave;ge plusieurs
   paquets ROHC ensemble afin de limiter au maximum le surco&ucirc;t de
   l'encapsulation IP.</p>

