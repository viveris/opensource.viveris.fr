<h1>OpenSAND</h1>
<h1>plateforme d'&eacute;mulation satellite</h1>

<h2>Pr&eacute;sentation</h2>
<p>
  OpenSAND est un outil d'ing&eacute;nierie et de recherche permettant
  d'&eacute;muler un syst&egrave;me de t&eacute;l&eacute;communication
  satellite.
</p>
<p>
 La facilit&eacute; de mise en oeuvre d'une telle plateforme en fait un
 candidat de choix pour la recherche dans le domaine des
 t&eacute;l&eacute;communications par satellite, que ce soit pour valider des
 fonctionnalit&eacute;s r&eacute;seau, pour l'&eacute;valuation de performances
 ou en tant que moyen de d&eacute;monstration.
</p>
<p>
 La plateforme est distibu&eacute;e sous les licenses GPLv3 et LGPLv3 (selon
 les modules) et est maintenue par un comit&eacute; de pilotage constitu&eacute;
 du CNES et de Thales Alenia Space.
</p>

<p>OpenSAND offre les fonctionnalit&eacute;s suivantes&nbsp;:</p>
<hr/>
<h2 class="centered">Fonctionnalit&eacute;s d'OpenSAND</h2>
<div class="row" id="opensand-features" style="text-align:left;">
  <div class="columns large-4">
    <em>R&eacute;seau&nbsp;:</em>
    <ul>
      <li>IPv4</li>
      <li>IPv6</li>
      <li>Multicast</li>
      <li>Ethernet</li>
      <li>Compression RoHC</li>
    </ul>
  </div>
  <div class="columns large-4">
    <em>Qualit&eacute; de service&nbsp;:</em>
    <ul>
      <li>Interface QoS IP (DSCP)</li>
      <li>Files MAC</li>
    </ul>
  </div>
  <div class="columns large-4"><em>Gestion de ressources/allocation MF-TDMA&nbsp;:</em>
    <ul>
      <li>Requ&ecirc;tes de capacit&eacute;</li>
      <li>CRA</li>
      <li>RBDC, VBDC</li>
      <li>FCA</li>
    </ul>
  </div>
  <div class="columns large-4">
    <em>Encapsulation&nbsp;:</em>
    <ul>
      <li>AAL5/ATM</li>
      <li>ULE/MPEG2-TS</li>
      <li>GSE</li>
    </ul>
  </div>
  <div class="columns large-4">
    <em>Couche physique&nbsp;:</em>
    <ul>
      <li>Codage et modulation adaptatifs (ACM)</li>
      <li>Emulations des canaux physiques</li>
    </ul>
  </div>
  <div class="columns large-4">
    <em>Satellite&nbsp;:</em>
    <ul>
      <li>Transparent (r&eacute;seau &eacute;toil&eacute;)</li>
      <li>Regeneratif (r&eacute;seau maillé)</li>
    </ul>
  </div>
</div>

<hr/>
<p>
  La plateforme OpenSAND peut &ecirc;tre d&eacute;ploy&eacute;e sur 4 machines
  Linux inter-connect&eacute;es sur un r&eacute;seau Ethernet IEEE802.3 classique
  comme pr&eacute;sent&eacute; ci-dessous. Le mat&eacute;riel n&eacute;cessaire
  est donc courant et peu on&eacute;reux.
</p>
