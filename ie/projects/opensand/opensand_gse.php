Librairie GSE<br>Generic Stream Encapsulation
</h1>

<h2>Pr&eacute;sentation</h2>
<p>GSE (Generic Stream Encapsulation) est un protocole d'encapsulation
   normalis&eacute; par l'ETSI dans le cadre du consortium DVB.</p>
<p>La librairie GSE est une impl&eacute;mentation de ce protocole effectu&eacute;e
   dans le respect de la norme et des recommandations d'impl&eacute;mentation
   publi&eacute; par l'ETSI. Elle est notamment utilis&eacute;e dans le cadre du
   projet OpenSAND comme m&eacute;thode d'encapsulation sur un lien DVB-S2.</p>

<h2>Informations techniques</h2>
<p>Les ressources relatives au projet sont disponibles sur la
   <a href="https://launchpad.net/libgse/" title="Launchpad GSE">forge du projet GSE</a>.</p>
